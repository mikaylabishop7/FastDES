static uint32_t rotl32(uint32_t x, int n) {
    return (x << n) | (x >> (32 - n));
}

static uint64_t rotl56(uint64_t x, int n) {
    x &= KEY_MASK_56;
    return ((x << n) | (x >> (56 - n))) & KEY_MASK_56;
}

static uint32_t permute32(uint32_t x) {
    return rotl32(x, 3) ^ rotl32(x, 11) ^ rotl32(x, 19);
}

static uint32_t feistel_f(uint32_t right, uint32_t round_key) {
    uint32_t x = right ^ round_key;

    uint8_t b0 = (x >> 24) & 0xFF;
    uint8_t b1 = (x >> 16) & 0xFF;
    uint8_t b2 = (x >> 8)  & 0xFF;
    uint8_t b3 = x & 0xFF;

    uint32_t y = 0;
    y |= ((uint32_t)SBOX1[b0]) << 24;
    y |= ((uint32_t)SBOX2[b1]) << 16;
    y |= ((uint32_t)SBOX3[b2]) << 8;
    y |= ((uint32_t)SBOX4[b3]);

    return permute32(y);
}

static void generate_round_keys(uint64_t master_key, uint32_t round_keys[NUM_ROUNDS]) {
    int shifts[NUM_ROUNDS] = {1, 2, 1, 2, 1, 2, 1, 2};
    uint64_t key = master_key & KEY_MASK_56;

    for (int i = 0; i < NUM_ROUNDS; i++) {
        key = rotl56(key, shifts[i]);
        round_keys[i] = (uint32_t)((key >> 24) & 0xFFFFFFFFU);
    }
}

uint64_t fastdes_encrypt(uint64_t plaintext, uint64_t master_key) {
    uint32_t round_keys[NUM_ROUNDS];
    generate_round_keys(master_key, round_keys);

    uint32_t L = (uint32_t)(plaintext >> 32);
    uint32_t R = (uint32_t)(plaintext & 0xFFFFFFFFU);

    for (int i = 0; i < NUM_ROUNDS; i++) {
        uint32_t newL = R;
        uint32_t newR = L ^ feistel_f(R, round_keys[i]);
        L = newL;
        R = newR;
    }

    return ((uint64_t)R << 32) | L;
}

uint64_t fastdes_decrypt(uint64_t ciphertext, uint64_t master_key) {
    uint32_t round_keys[NUM_ROUNDS];
    generate_round_keys(master_key, round_keys);

    uint32_t R = (uint32_t)(ciphertext >> 32);
    uint32_t L = (uint32_t)(ciphertext & 0xFFFFFFFFU);

    for (int i = NUM_ROUNDS - 1; i >= 0; i--) {
        uint32_t oldR = L;
        uint32_t oldL = R ^ feistel_f(L, round_keys[i]);
        R = oldR;
        L = oldL;
    }

    return ((uint64_t)L << 32) | R;
}

static void trim_newline(char *s) {
    size_t len = strlen(s);
    if (len > 0 && s[len - 1] == '\n') {
        s[len - 1] = '\0';
    }
}

static int is_valid_hex_string(const char *s) {
    if (*s == '\0') return 0;
    while (*s) {
        if (!isxdigit((unsigned char)*s)) {
            return 0;
        }
        s++;
    }
    return 1;
}

static int read_line(const char *prompt, char *buffer, size_t size) {
    printf("%s", prompt);
    if (fgets(buffer, size, stdin) == NULL) {
        return 0;
    }
    trim_newline(buffer);
    return 1;
}

static int parse_hex64(const char *s, uint64_t *value) {
    if (strncmp(s, "0x", 2) == 0 || strncmp(s, "0X", 2) == 0) {
        s += 2;
    }

    if (!is_valid_hex_string(s)) {
        return 0;
    }

    if (strlen(s) == 0 || strlen(s) > 16) {
        return 0;
    }

    if (sscanf(s, "%" SCNx64, value) != 1) {
        return 0;
    }

    return 1;
}

static char *pad_left_to_block_multiple(const char *hex) {
    size_t len = strlen(hex);
    size_t padded_len = ((len + 15) / 16) * 16;
    size_t pad = padded_len - len;

    char *result = malloc(padded_len + 1);
    if (!result) {
        return NULL;
    }

    for (size_t i = 0; i < pad; i++) {
        result[i] = '0';
    }
    memcpy(result + pad, hex, len);
    result[padded_len] = '\0';

    return result;
}

static int process_hex_string_blocks(const char *input_hex, uint64_t key, int encrypt_mode, char *output_hex, size_t output_size) {
    char *padded = NULL;
    size_t len, blocks;

    if (!is_valid_hex_string(input_hex)) {
        return 0;
    }

    padded = pad_left_to_block_multiple(input_hex);
    if (!padded) {
        return 0;
    }

    len = strlen(padded);
    blocks = len / 16;

    if (output_size < len + 1) {
        free(padded);
        return 0;
    }

    output_hex[0] = '\0';

    for (size_t i = 0; i < blocks; i++) {
        char block_str[17];
        uint64_t block_value;
        uint64_t result;

        memcpy(block_str, padded + (i * 16), 16);
        block_str[16] = '\0';

        if (!parse_hex64(block_str, &block_value)) {
            free(padded);
            return 0;
        }

        if (encrypt_mode) {
            result = fastdes_encrypt(block_value, key);
        } else {
            result = fastdes_decrypt(block_value, key);
        }

        char temp[17];
        snprintf(temp, sizeof(temp), "%016" PRIX64, result);
        strcat(output_hex, temp);
    }

    free(padded);
    return 1;
}

int main(void) {
    char choice[10];
    char key_str[100];
    char data_str[MAX_INPUT_LEN];
    char result_str[MAX_INPUT_LEN + 16];
    uint64_t key;

    printf("FastDES Menu\n");
    printf("------------\n");
    printf("E = Encrypt\n");
    printf("D = Decrypt\n");

    if (!read_line("Enter choice: ", choice, sizeof(choice))) {
        printf("Input error.\n");
        return 1;
    }

    if (!read_line("Enter hex key (up to 16 hex digits): ", key_str, sizeof(key_str))) {
        printf("Input error.\n");
        return 1;
    }

    if (!parse_hex64(key_str, &key)) {
        printf("Invalid key.\n");
        return 1;
    }

    key &= KEY_MASK_56;

    if (choice[0] == 'E' || choice[0] == 'e') {
        if (!read_line("Enter plaintext as hex (any length): ", data_str, sizeof(data_str))) {
            printf("Input error.\n");
            return 1;
        }

        if (!process_hex_string_blocks(data_str, key, 1, result_str, sizeof(result_str))) {
            printf("Invalid plaintext hex input.\n");
            return 1;
        }

        printf("Ciphertext: %s\n", result_str);
    }
    else if (choice[0] == 'D' || choice[0] == 'd') {
        if (!read_line("Enter ciphertext as hex (any length): ", data_str, sizeof(data_str))) {
            printf("Input error.\n");
            return 1;
        }

        if (!process_hex_string_blocks(data_str, key, 0, result_str, sizeof(result_str))) {
            printf("Invalid ciphertext hex input.\n");
            return 1;
        }

        printf("Plaintext:  %s\n", result_str);
    }
    else {
        printf("Invalid choice. Enter E or D.\n");
        return 1;
    }

    return 0;
}